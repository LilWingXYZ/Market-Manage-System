    public void showOnTable(List<Customer> list){
        //获取制定（tblProduct）表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblProduct.getModel();
        //2清空表格信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        //显示数据
        for(Customer p : list){
            //颜色警戒
            colorTableRender colorRender = new colorTableRender ();  
            tblProduct.getColumn("累计消费额度").setCellRenderer(colorRender);
            Vector vt = new Vector();
            vt.add(p.getCid());
            vt.add(p.getCname());
            vt.add(p.getCtel());
            vt.add(p.getMoney());
            dtm.addRow(vt);
        }
    }

