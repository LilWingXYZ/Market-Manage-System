    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
        int cid = Integer.parseInt(this.txtCid.getText());
        String cname = this.txtCname.getText().trim();

        float cmoney = Float.parseFloat(this.txtCmoney.getText().trim());
        String ctel = this.txtCtel.getText().trim();

        //数据格式验证使用StringUtil
        //1验证非空
        try{
        if(StringUtil.checkLength(cname)==false){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(cid == 0){
            JOptionPane.showMessageDialog(this, "库存需为数字格式");
            return;
        }
        if(StringUtil.checkDigit(ctel)==false){
                JOptionPane.showMessageDialog(this, "库存需为数字格式");
                return;
        }
        if(cmoney == 0){
                JOptionPane.showMessageDialog(this, "库存需为数字格式");
                return;
        }
        }catch(Exception e){
            return;
        }

        Customer p = new Customer();
        p.setCid(cid);
        p.setCname(cname);
        p.setCtel(ctel);
        p.setMoney(cmoney);
        DBLink lk = new DBLink(); 
        Connection conn = lk.getConnection();
        boolean result = false;
        try {
            result = pbiz.add(conn,p);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerADCS.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(CustomerADCS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Customer>list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          
        int answer = JOptionPane.showConfirmDialog(this, "您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            int proid = Integer.parseInt(this.txtCid.getText());
            DBLink lk = new DBLink();
            Connection conn = lk.getConnection();
            boolean result = false;
            try {
                result = result = pbiz.delete(conn,proid);
            } catch (SQLException ex) {
                Logger.getLogger(CustomerADCS.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(CustomerADCS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(result){
                JOptionPane.showMessageDialog(this, "删除成功");
                List<Customer>list = pbiz.findAll();
                //显示list中的信息
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this, "删除失败");
            }
        }
    }                                         

