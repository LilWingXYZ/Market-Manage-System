    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
        int sid = Integer.parseInt(this.txtSid.getText());
        String Saddr = this.txtSaddr.getText();
        String Stel = this.txtTel.getText();
        String Sname = this.txtSname.getText();
        //数据格式验证使用StringUtil
        //1验证非空
        try{
        if(StringUtil.checkLength(Saddr)==false){
            JOptionPane.showMessageDialog(this, "供应商地址为空");
            return;
        }
        if(StringUtil.checkLength(Stel)==false){
            JOptionPane.showMessageDialog(this, "供应商电话为空");
            return;
        }
        if(StringUtil.checkLength(Sname)==false){
            JOptionPane.showMessageDialog(this, "供应商名称为空");
            return;
        }
        if(sid == 0){
            JOptionPane.showMessageDialog(this, "供应商编号为空");
            return;
        }
        }catch(Exception e){
            return;
        }
        Supplier p = new Supplier();
        p.setSid(sid);
        p.setSaddr(Saddr);
        p.setStel(Stel);
        p.setSname(Sname);
        DBLink lk = new DBLink();
        Connection conn =  lk.getConnection();
        boolean result = false;
        try {
            result = pbiz.add(conn,p);
        } catch (SQLException ex) {
            Logger.getLogger(SupplierADCS.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(SupplierADCS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Supplier>list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          
        int answer = JOptionPane.showConfirmDialog(this, "您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            int proid = Integer.parseInt(this.txtSid.getText());
            DBLink lk = new DBLink();
            Connection conn = lk.getConnection();
            boolean result = false;
            try {
                result = pbiz.delete(conn,proid);
            } catch (SQLException ex) {
                Logger.getLogger(SupplierADCS.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(SupplierADCS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(result){
                JOptionPane.showMessageDialog(this, "删除成功");
                List<Supplier>list = pbiz.findAll();
                //显示list中的信息
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this, "删除失败");
            }
        }
    }                                         

