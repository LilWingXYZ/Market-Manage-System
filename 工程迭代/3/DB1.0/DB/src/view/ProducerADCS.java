    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
        int pid = Integer.parseInt(this.txtPid.getText().trim());
        String pname = this.txtPname.getText();
        String ptel = this.txtPtel.getText().trim();
        String paddr = this.txtPaddr.getText().trim();

        //数据格式验证使用StringUtil
        //1验证非空
        try{
            if(pid == 0){
                JOptionPane.showMessageDialog(this, "商品不能为空");
                return;
            }
        if(StringUtil.checkLength(pname)==false){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(StringUtil.checkLength(ptel)==false){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(StringUtil.checkDigit(paddr)==false){
                JOptionPane.showMessageDialog(this, "库存需为数字格式");
                return;
        }
 
        }catch(Exception e){
            return;
        }

        Producer p = new Producer();
        p.setPaddr(paddr);
        p.setPid(pid);
        p.setPname(pname);
        p.setPtel(ptel);
        DBLink lk = new DBLink();
        Connection conn = lk.getConnection();
        boolean result = false;
        try {
            result = pbiz.add(conn,p);
        } catch (SQLException ex) {
            Logger.getLogger(ProducerADCS.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(ProducerADCS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Producer>list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          
        int answer = JOptionPane.showConfirmDialog(this, "您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            int proid = Integer.parseInt(this.txtPid.getText());
            DBLink lk = new DBLink();
            Connection conn = lk.getConnection();
            boolean result= false;
            try {
                result = result = pbiz.delete(conn,proid);
            } catch (SQLException ex) {
                Logger.getLogger(ProducerADCS.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(ProducerADCS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(result){
                JOptionPane.showMessageDialog(this, "删除成功");
                List<Producer>list = pbiz.findAll();
                //显示list中的信息
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this, "删除失败");
            }
        }
    }                                         

