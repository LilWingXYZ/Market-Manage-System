    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
        SimpleDateFormat sf  = new SimpleDateFormat("yy-mm-dd");
        int gid = Integer.parseInt(this.txtGid.getText());
        String gname = this.txtGname.getText();
        float gpprice = Float.parseFloat(this.txtGpprice.getText());
        float gsprice = Float.parseFloat(this.txtGsprice.getText());
        int pid = Integer.parseInt(this.txtPid.getText());
        Date dt = null;
        try {
            dt = sf.parse(this.txtForm.getText());
        } catch (ParseException ex) {
            Logger.getLogger(GoodsADCS.class.getName()).log(Level.SEVERE, null, ex);
        }
        //数据格式验证使用StringUtil
        //1验证非空
        try{
        if(StringUtil.checkLength(gname)==false){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(gid == 0){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(pid == 0){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(gpprice == 0.0){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(gsprice == 0.0){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        if(dt == null){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }
        /*
        if(pbiz.isRepeat(pid)){
            JOptionPane.showMessageDialog(this, "该单元已经存在，无须再创建");
            return;
        }
        */
        }catch(Exception e){
            return;
        }
        Goods p = new Goods();
        p.setDatetime(dt);
        p.setGid(gid);
        p.setGname(gname);
        p.setPid(pid);
        p.setGpprice(gpprice);
        p.setGsprice(gsprice);
        DBLink lk = new DBLink();
        Connection conn = lk.getConnection();
        boolean result = false;
        try {
            result = pbiz.add(conn,p);
        } catch (SQLException ex) {
            Logger.getLogger(GoodsADCS.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(GoodsADCS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Goods>list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          
        int answer = JOptionPane.showConfirmDialog(this, "您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            int proid = Integer.parseInt(this.txtGid.getText());
            DBLink lk = new DBLink(); 
            Connection conn = lk.getConnection();
            boolean result = false;
            try {
                result = pbiz.delete(conn,proid);
            } catch (SQLException ex) {
                Logger.getLogger(GoodsADCS.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(GoodsADCS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(result){
                JOptionPane.showMessageDialog(this, "删除成功");
                List<Goods>list = pbiz.findAll();
                //显示list中的信息
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this, "删除失败");
            }
        }
    }                                         

