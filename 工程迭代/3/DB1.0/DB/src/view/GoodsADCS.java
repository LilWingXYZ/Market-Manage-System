    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {                                        
        //鼠标选中某行，改行信息显示到输出面板
        int row = this.tblProduct.getSelectedRow();
        //根据row获取每列的值
        this.txtGid.setText(this.tblProduct.getValueAt(row, 0)+"");
        this.txtGname.setText(this.tblProduct.getValueAt(row, 1)+"");
        this.txtGpprice.setText(this.tblProduct.getValueAt(row, 2)+"");
        this.txtGsprice.setText(this.tblProduct.getValueAt(row, 3)+"");
        this.txtPid.setText(this.tblProduct.getValueAt(row, 4)+"");
        this.txtForm.setText(this.tblProduct.getValueAt(row, 5)+"");
        //保存删除按钮可用
        this.btnSave.setEnabled(true);
        this.btnDelete.setEnabled(true);
    }                                       

