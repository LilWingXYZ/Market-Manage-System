    public void showOnTable(List<Employee> list){
        //获取制定（tblProduct）表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblProduct.getModel();
        //2清空表格信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        //显示数据
        for(Employee p : list){
            //颜色警戒
            Vector vt = new Vector();
            vt.add(p.getEid());
            vt.add(p.getEname());
            vt.add(p.getElevel());
            vt.add(p.getEtel());
            dtm.addRow(vt);
        }
    }

