    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {                                        
        //鼠标选中某行，改行信息显示到输出面板
        int row = this.tblProduct.getSelectedRow();
        //根据row获取每列的值
        this.txtEid.setText(this.tblProduct.getValueAt(row, 0)+"");
        this.txtEname1.setText(this.tblProduct.getValueAt(row, 1)+"");

        this.txtEtel.setText(this.tblProduct.getValueAt(row, 3)+"");
        this.txtElevel.setText(this.tblProduct.getValueAt(row, 2)+"");
        //保存删除按钮可用
        this.btnSave.setEnabled(true);
        this.btnDelete.setEnabled(true);
    }                                       

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
        int eid = Integer.parseInt(this.txtEid.getText());
        String ename = this.txtEname1.getText().trim();
        String etel = this.txtEtel.getText().trim();
        int elevel = Integer.parseInt(this.txtElevel.getText().trim());
        String epwd = this.txtEpwd.getText().trim();
        //数据格式验证使用StringUtil
        //1验证非空
        try{
            if(eid == 0){
                JOptionPane.showMessageDialog(this, "商品不能为空");
                return;
            }
        if(StringUtil.checkLength(ename)==false){
            JOptionPane.showMessageDialog(this, "商品不能为空");
            return;
        }

        if(elevel==0){
            JOptionPane.showMessageDialog(this, "建议购买价不能为空");
            return;
        }
        if(StringUtil.checkLength(etel)==false){
            JOptionPane.showMessageDialog(this, "建议销售价不能为空");
            return;
        }
        if(StringUtil.checkLength(epwd)==false){
            JOptionPane.showMessageDialog(this, "建议销售价不能为空");
            return;
        }
        }catch(Exception e){
            return;
        }
        Employee p = new Employee();
        p.setEid(eid);
        p.setElevel(elevel);
        p.setEname(ename);
        p.setEtel(etel);
        p.setPassword(epwd);
        DBLink lk = new DBLink();
        Connection conn = lk.getConnection();
        boolean result= false;
        try {
            result = pbiz.add(conn,p);
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeADCS.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(EmployeeADCS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Employee>list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          
        int answer = JOptionPane.showConfirmDialog(this, "您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            int proid = Integer.parseInt(this.txtEid.getText());
            boolean result = false;
            DBLink lk = new DBLink();
            Connection conn = lk.getConnection();
            try {
                result = pbiz.delete(conn,proid);
                } catch (SQLException ex) {
                Logger.getLogger(EmployeeADCS.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(EmployeeADCS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(result){
                JOptionPane.showMessageDialog(this, "删除成功");
                List<Employee>list = pbiz.findAll();
                //显示list中的信息
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this, "删除失败");
            }
        }
    }                                         

