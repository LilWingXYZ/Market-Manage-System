    public void showOnTable(List<Purchase> listp,List<BaseStore> listb){
        //获取制定（tblProduct）表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblProduct.getModel();
        //2清空表格信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        for(BaseStore b : listb){
        //显示数据
        for(Purchase p : listp){
            //颜色警戒
            if(b.getGid() == p.getGid()){
                Vector vt = new Vector();
                vt.add(p.getEid());
                vt.add(p.getGid());
                vt.add(p.getSid());
                vt.add(p.getGnum());
                vt.add(p.getPurdate());
                vt.add(b.getBid());
                dtm.addRow(vt);
            }
            }
        }
    }

