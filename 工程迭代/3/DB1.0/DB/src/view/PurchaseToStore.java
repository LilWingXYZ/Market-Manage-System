    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd ");
        int bid = Integer.parseInt(this.txtBid.getText());
        int eid = Integer.parseInt(this.txtEid.getText());
        int gid = Integer.parseInt(this.txtGid.getText());
        int gnum = Integer.parseInt(this.txtGnum.getText());
        int sid = Integer.parseInt(this.txtPid.getText());
        Date dt = null;
        try {
            dt = sdf.parse(this.txtDate.getText());
        } catch (ParseException ex) {
            Logger.getLogger(PurchaseToStore.class.getName()).log(Level.SEVERE, null, ex);
        }
        //数据格式验证使用StringUtil
        //1验证非空
        try{
                        if(bid == 0){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
                return;
            }
            if(eid == 0){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
                return;
            }
            if(gid == 0){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
                return;
            }
            if(sid == 0){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
                return;
            }
            if(gnum == 0){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
                return;
            }
            if(dt == null){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
                return;
            }
        }catch(Exception e){
            return;
        }

        DBLink lk = new DBLink();
        Connection conn = lk.getConnection();
        boolean result = false;
        BaseStore p = new BaseStore();
        Purchase t = new Purchase();
        t.setEid(eid);
        t.setGid(gid);
        t.setSid(sid);
        p.setBid(bid);
        p.setGid(gid);
        p.setGnum(gnum);
        t.setPurdate(dt);
        try {
            result = bsbiz.add(conn,p);
            result = pbiz.add(conn, t);
        } catch (SQLException ex) {
            Logger.getLogger(PurchaseToStore.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(PurchaseToStore.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Purchase>listp = pbiz.findAll();
            List<BaseStore> listb = bsbiz.findAll();
            showOnTable(listp,listb);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

